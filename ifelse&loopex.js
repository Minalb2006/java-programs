
//If else example//
let colo=prompt("Enter the color of traffic light:");
if(colo=="red")
{
    console.log("Stop");
}else if(colo=="yellow")
{
    console.log("Ready");
}else if(colo=="green")
{
    console.log("Go");
}else{
    console.log("Invalid color");
}

